<template>
    <div class="row page-titles">
        <div class="col-md-5 align-self-center">
            <h3 class="text-themecolor">Listagem de Clientes</h3>
        </div>
        <div class="col-md-7 align-self-center">
            <ol class="breadcrumb">
                <li class="breadcrumb-item"><a href="javascript:void(0)">Inicio</a></li>
                <li class="breadcrumb-item">Listagem</li>
                <li class="breadcrumb-item active">Clientes</li>
            </ol>
        </div>
    </div>
    <div class="container-fluid">
        <div class="row">
            <div class="col-12">
                <div class="card">
                    <div class="card-body">
                        <h4 class="card-title">Visualização de Dados</h4>
                        <h6 class="card-subtitle">Export data to Copy, CSV, Excel, PDF & Print</h6>
                        <div class="table-responsive m-t-40">
                            <div class="row">
                                <div class="col-6">
                                    <div class="form-group row">
                                        <label for="start-date" class="col-md-2 col-form-label">Mês Inicio</label>
                                        <div class="col-md-10">
                                            <VueDatePicker :model-value="startDate" month-picker
                                                @update:model-value="startDate => handleDateUpdate('start', startDate)" />
                                        </div>
                                    </div>
                                </div>
                                <div class="col-6">
                                    <div class="form-group row">
                                        <label for="end-date" class="col-md-2 col-form-label">Mês Fim</label>
                                        <div class="col-md-10">
                                            <VueDatePicker v-model="endDate" month-picker
                                                @update:model-value="endDate => handleDateUpdate('end', endDate)" />
                                        </div>
                                    </div>
                                </div>
                            </div>
                            <div class="row">
                                <div class="col-md-2"></div>
                                <div class="col-md-3">
                                    <button @click="viewSelected(1)" class="btn btn-outline-success btn-rounded"><i
                                            class="fa fa-table"></i> Ver
                                        Tabelas</button>
                                </div>
                                <div class="col-md-3">
                                    <button @click="viewSelected(2)" class="btn btn-outline-warning btn-rounded"><i
                                            class="fa fa-pie-chart"></i> Ver
                                        Gráfico Pizza</button>
                                </div>
                                <div class="col-md-4">
                                    <button @click="viewSelected(3)" class="btn btn-outline-danger btn-rounded"><i
                                            class="fa fa-bar-chart"></i> Ver
                                        Gráfico Barras</button>
                                </div>
                            </div>
                            <table id="myTable" class="display nowrap table table-hover table-striped table-bordered"
                                cellspacing="0" width="100%">
                                <thead>
                                    <tr>
                                        <th>
                                            <input type="checkbox" id="select_all" v-model="selectAll"
                                                @change="toggleSelectAll" />
                                            <label for="select_all"><strong>All</strong></label>
                                        </th>
                                        <th>NID</th>
                                        <th>Nome Completo</th>
                                        <th>Codigo</th>
                                        <th>Endereço</th>
                                        <th>CPNJ</th>
                                        <th>Telefone</th>
                                        <!-- <th>Acções</th> -->
                                    </tr>
                                </thead>
                                <tfoot>
                                    <tr>
                                        <th><input type="checkbox" name="" id=""></th>
                                        <th>NID</th>
                                        <th>Nome Completo</th>
                                        <th>Codigo</th>
                                        <th>Endereço</th>
                                        <th>CPNJ</th>
                                        <th>Telefone</th>
                                        <!-- <th>Acções</th> -->
                                    </tr>
                                </tfoot>
                                <tbody v-if="this.clients.length > 0">
                                    <tr v-for="(value, index) in this.clients" :key="index">
                                        <td>
                                            <input type="checkbox" :id="'select_' + (index + 1)" v-model="selectedIds"
                                                :value="index + 1" />
                                            <label :for="'select_' + (index + 1)"></label>
                                        </td>

                                        <td>{{ index + 1 }}</td>
                                        <td>{{ value.no_razao }}</td>
                                        <td>{{ value.co_cliente }}</td>
                                        <td>{{ value.ds_endereco }}</td>
                                        <td>{{ value.nu_cnpj }}</td>
                                        <td>{{ value.nu_telefone }}</td>
                                    </tr>
                                </tbody>
                                <tbody v-else>
                                    <tr>
                                        <td colspan="7">Loading...</td>
                                    </tr>
                                </tbody>
                            </table>
                        </div>
                    </div>
                </div>
            </div>
        </div>
    </div>
</template>

<script>
import http from '@/services/http';
import Swal from 'sweetalert2';

export default {
    name: 'clientsList',
    data() {
        return {
            clients: [],
            selectAll: false,
            selectedIds: [],
            startDate: null,
            endDate: null
        }
    },
    mounted() {
        this.getClient();
    },
    methods: {

        getClient() {
            http.get('clientes')
                .then((response) => {
                    this.clients = response.data;
                })
                .catch((error) => {
                    console.error('Error fetching cliente:', error);
                });
        },

        toggleSelectAll() {
            this.selectedIds = this.selectAll ? this.clients.map((_, index) => index + 1) : [];
        },

        handleDateUpdate(type, date) {
            const selectedDate = date;
            const month = selectedDate.month + 1;
            const year = selectedDate.year;

            if (month < 1 || month > 12 || isNaN(month) || isNaN(year)) {
                this.showSweetAlert('Erro de Inserção', 'Porfavor, Selecione uma data válida', 'error');
                return;
            }

            if (type === 'start') {
                this.startDate = `${year}-${month.toString().padStart(2, '0')}`;
            } else if (type === 'end') {
                this.endDate = `${year}-${month.toString().padStart(2, '0')}`;
            }

            console.log('Start Date:', this.startDate);
            console.log('End Date:', this.endDate);
        },

        viewSelected(param) {

            if (!this.startDate || !this.endDate || this.selectedIds.length === 0) {
                this.showSweetAlert('Erro de Validação', 'Porfavor, Selecione um cliente e/ou preencha as datas', 'error');
                return;
            }

            this.submitForm(param);
        },

        submitForm(operation) {

            const selectedData = {
                startDate: this.startDate,
                endDate: this.endDate,
                selectedIds: this.selectedIds.join(','),
            };

            if (operation == 1) {
                this.$router.push({
                    name: 'ClientDetails',
                    params: { id: operation },
                    query: selectedData,
                });
            } else if (operation == 2) {
                this.$router.push({
                    name: 'ClientPieDetails',
                    params: { id: operation },
                    query: selectedData,
                });
            } else if (operation == 3) {
                this.$router.push({
                    name: 'ClientBarDetails',
                    params: { id: operation },
                    query: selectedData,
                });
            } else {
                this.showSweetAlert('Erro de Validação', 'Porfavor, Verifique os parametros da tua seleção! Não reconhecemos essa entrada', 'error');
                return;
            }
        },

        showSweetAlert(header, message, type) {
            Swal.fire({
                text: message,
                icon: type,
                timer: 3000,
                timerProgressBar: true,
                title: header,
                confirmButtonText: 'OK',
            });
        }
    }
};

</script>

